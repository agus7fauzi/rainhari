package plugin

import (
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/proto"
	"google.golang.org/protobuf/types/descriptorpb"
)

const (
	contextPackage     = protogen.GoImportPath("context")
	serverPackage      = protogen.GoImportPath("github.com/agus7fauzi/rainhari/core/server")
	clientPackage      = protogen.GoImportPath("github.com/agus7fauzi/rainhari/core/client")
	deprecationComment = "// Deprecated: Do not use."
)

var reversedClientName = map[string]bool{}

// GenerateFile generates *.rainhari.pb.go files containing gRPC service definitions.
func GenerateFile(gen *protogen.Plugin, file *protogen.File) *protogen.GeneratedFile {
	filename := file.GeneratedFilenamePrefix + ".rainhari.pb.go"
	g := gen.NewGeneratedFile(filename, file.GoImportPath)

	g.P("// Code generated by protoc-gen-rainhari. DO NOT EDIT.")
	g.P()
	g.P("package ", file.GoPackageName)
	g.P()

	generateCoreCode(gen, file, g)

	return g
}

func generateCoreCode(gen *protogen.Plugin, file *protogen.File, g *protogen.GeneratedFile) {
	// Iterate through all the `service`s in the passed-in file
	for i, service := range file.Services {
		generateService(gen, file, g, service, i)
	}
}

func generateService(gen *protogen.Plugin, file *protogen.File, g *protogen.GeneratedFile, service *protogen.Service, index int) {
	originSvcName := service.GoName
	serviceName := strings.ToLower(originSvcName)

	if pkg := file.GoPackageName; pkg != "" {
		serviceName = string(pkg)
	}

	cCaseSvcName := camelCase(originSvcName)
	serviceAlias := cCaseSvcName + "Service"

	// Strip suffix
	if strings.HasSuffix(serviceAlias, "ServiceService") {
		serviceAlias = strings.TrimSuffix(serviceAlias, "Service")
	}

	// Client interface
	g.P("type ", serviceAlias, " interface {")
	for _, method := range service.Methods {
		g.Annotate(serviceName+"."+method.GoName, method.Location)
		if method.Desc.Options().(*descriptorpb.MethodOptions).GetDeprecated() {
			g.P(deprecationComment)
		}
		g.P(method.Comments.Leading, generateClientSignature(g, method, serviceName))
	}
	g.P("}")
	g.P()

	// Client struct
	g.P("type ", unexport(serviceAlias), " struct {")
	g.P("c " + g.QualifiedGoIdent(clientPackage.Ident("Client")))
	g.P("name string")
	g.P("}")
	g.P()

	// NewClient factory
	g.P("func New", serviceAlias, " (name string, c "+g.QualifiedGoIdent(clientPackage.Ident("Client"))+") ", serviceAlias, " {")
	g.P("return &", unexport(serviceAlias), "{")
	g.P("c: c,")
	g.P("name: name,")
	g.P("}")
	g.P("}")
	g.P()

	var methodIndex, streamIndex int
	serviceDesc := "_" + serviceName + "_serviceDesc"
	// Client method implementations
	for _, method := range service.Methods {
		var descExpr string
		if !method.Desc.IsStreamingServer() {
			// Unary RPC method
			descExpr = "&" + serviceDesc + ".Methods[" + string(rune(methodIndex)) + "]"
			methodIndex++
		} else {
			// Streaming RPC method
			descExpr = "&" + serviceDesc + ".Methods[" + string(rune(streamIndex)) + "]"
			streamIndex++
		}
		generateClientMethod(gen, file, g, method, serviceName, cCaseSvcName, serviceDesc, descExpr)
	}

	g.P("// Server API for ", serviceName, " service")
	g.P()

	// Server instance
	serverType := service.GoName + "Handler"
	g.P("// ", serverType, " is the server API for ", service.GoName, " service.")
	g.P("type ", serverType, " interface {")
	for _, method := range service.Methods {
		g.Annotate(serverType+"."+method.GoName, method.Location)
		g.P(generateServerSignature(g, method, serviceName))
	}
	g.P("}")
	g.P()

	// Server registration
	g.P("func Register", cCaseSvcName, "Handler(s ", g.QualifiedGoIdent(serverPackage.Ident("Server")), ", hdlr ", serverType, ", opts ...", g.QualifiedGoIdent(serverPackage.Ident("HandlerOption")), ") error {")
	g.P("type ", unexport(cCaseSvcName), " interface {")

	// Generate interface methods
	for _, method := range service.Methods {
		methodName := camelCase(method.GoName)
		inType := g.QualifiedGoIdent(method.Input.GoIdent)
		outType := g.QualifiedGoIdent(method.Output.GoIdent)

		if !method.Desc.IsStreamingServer() && !method.Desc.IsStreamingClient() {
			g.P(methodName, "(ctx ", g.QualifiedGoIdent(contextPackage.Ident("Context")), ", in *", inType, ", out *", outType, ") error")
			continue
		}
		g.P(methodName, "(ctx ", g.QualifiedGoIdent(contextPackage.Ident("Context")), ", stream server.Stream) error")
	}
	g.P("}")
	g.P("type ", cCaseSvcName, " struct {")
	g.P(unexport(cCaseSvcName))
	g.P("}")
	g.P("h := &", unexport(cCaseSvcName), "Handler{hdlr}")
	for _, method := range service.Method {
		if method.Options != nil && proto.HasExtension(method.Options, options.E_Http) {
			g.P("opts = append(opts, ", apiPkg, ".WithEndpoint(&", apiPkg, ".Endpoint{")
			g.generateEndpoint(cCaseSvcName, method)
			g.P("}))")
		}
	}
	g.P("return s.Handle(s.NewHandler(&", cCaseSvcName, "{h}, opts...))")
	g.P("}")
	g.P()

	g.P("type ", unexport(cCaseSvcName), "Handler struct {")
	g.P(serverType)
	g.P("}")
}

func generateClientMethod(gen *protogen.Plugin, file *protogen.File, g *protogen.GeneratedFile, method *protogen.Method, reqSvc, cCaseSvcName, serviceDesc string, descExpr string) {
	reqMethod := cCaseSvcName + "." + method.GoName
	methName := camelCase(method.GoName)
	inType := method.Input.GoIdent
	outType := method.Output.GoIdent

	serviceAlias := cCaseSvcName + "Service"

	// strip suffix
	if strings.HasSuffix(serviceAlias, "ServiceService") {
		serviceAlias = strings.TrimSuffix(serviceAlias, "Service")
	}

	g.P("func (c *", unexport(serviceAlias), ") ", generateClientSignature(g, method, cCaseSvcName), "{")
	if !method.Desc.IsStreamingServer() && !method.Desc.IsStreamingClient() {
		g.P(`req := c.c.NewRequest(c.name, "`, reqMethod, `", in)`)
		g.P("out := new(", outType, ")")
		g.P("err := ", `c.c.Call(ctx, req, out, opts...)`)
		g.P("if err != nil { return nil, err }")
		g.P("return out, nil")
		g.P("}")
		g.P()
		return
	}
	streamType := unexport(serviceAlias) + methName
	g.P(`req := c.c.NewRequest(c.name, "`, reqMethod, `", &`, inType, `{})`)
	g.P("stream, err := c.c.Stream(ctx, req, opts...)")
	g.P("if err != nil { return nil, err }")

	if !method.Desc.IsStreamingClient() {
		g.P("if err := stream.Send(in); err != nil { return nil, err }")
	}

	g.P("return &", streamType, "{stream}, nil")
	g.P("}")
	g.P()

	genSend := method.Desc.IsStreamingClient()
	genRecv := method.Desc.IsStreamingServer()

	// Stream auxiliary types and methods
	g.P("type ", cCaseSvcName, "_", methName, "Service interface {")
	g.P("Context() context.Context")
	g.P("SendMsg(interface{}) error")
	g.P("RecvMsg(interface{}) error")
	g.P("Close() error")

	if genSend {
		g.P("Send(*", inType, ") error")
	}
	if genRecv {
		g.P("Recv() (*", outType, ", error)")
	}
	g.P("}")
	g.P()

	g.P("type ", streamType, " struct {")
	g.P("stream ", g.QualifiedGoIdent(clientPackage.Ident("Stream")))
	g.P("}")
	g.P()

	g.P("func (x *", streamType, ") Close() error {")
	g.P("return x.stream.Close()")
	g.P("}")
	g.P()

	g.P("func (x *", streamType, ") Context() context.Context {")
	g.P("return x.stream.Context()")
	g.P("}")
	g.P()

	g.P("func (x *", streamType, ") SendMsg(m interface{}) error {")
	g.P("return x.stream.Send(m)")
	g.P("}")
	g.P()

	g.P("func (x *", streamType, ") RecvMsg(m interface{}) error {")
	g.P("return x.stream.Recv(m)")
	g.P("}")
	g.P()

	if genSend {
		g.P("func (x *", streamType, ") Send(m *", inType, ") error {")
		g.P("return x.stream.Send(m)")
		g.P("}")
		g.P()

	}

	if genRecv {
		g.P("func (x *", streamType, ") Recv() (*", outType, ", error) {")
		g.P("m := new(", outType, ")")
		g.P("err := x.stream.Recv(m)")
		g.P("if err != nil {")
		g.P("return nil, err")
		g.P("}")
		g.P("return m, nil")
		g.P("}")
		g.P()
	}
}

func generateClientSignature(g *protogen.GeneratedFile, method *protogen.Method, serviceName string) string {
	originMethodName := method.GoName
	methodName := camelCase(originMethodName)

	if reversedClientName[methodName] {
		methodName += "_"
	}
	reqArg := ", in *" + g.QualifiedGoIdent(method.Input.GoIdent)
	if method.Desc.IsStreamingClient() {
		reqArg = ""
	}
	rspName := "*" + g.QualifiedGoIdent(method.Output.GoIdent)
	if method.Desc.IsStreamingClient() || method.Desc.IsStreamingServer() {
		rspName = serviceName + "_" + camelCase(originMethodName) + "Service"
	}

	return methodName + "(ctx " + g.QualifiedGoIdent(contextPackage.Ident("Context")) + reqArg + ", opts ..." + g.QualifiedGoIdent(clientPackage.Ident("CallOption")) + ") (" + rspName + ", error)"
}

func generateServerSignature(g *protogen.GeneratedFile, method *protogen.Method, serviceName string) string {
	oriMethodName := method.GoName
	methodName := camelCase(oriMethodName)

	var reqArgs []string
	ret := "error"
	reqArgs = append(reqArgs, g.QualifiedGoIdent(contextPackage.Ident("Context")))

	if !method.Desc.IsStreamingClient() {
		reqArgs = append(reqArgs, "*"+g.QualifiedGoIdent(method.Input.GoIdent))
	}
	if method.Desc.IsStreamingServer() || method.Desc.IsStreamingClient() {
		reqArgs = append(reqArgs, serviceName+"_"+camelCase(oriMethodName)+"Stream")
	}
	if !method.Desc.IsStreamingClient() && !method.Desc.IsStreamingServer() {
		reqArgs = append(reqArgs, "*"+g.QualifiedGoIdent(method.Output.GoIdent))
	}

	return methodName + "(" + strings.Join(reqArgs, ", ") + ") " + ret
}

func isASCIILower(c byte) bool {
	return 'a' <= c && c <= 'z'
}

func isASCIIDigit(c byte) bool {
	return '0' <= c && c <= '9'
}

func camelCase(s string) string {
	if s == "" {
		return ""
	}
	t := make([]byte, 0, 32)
	i := 0
	if s[0] == '_' {
		// Need a capital letter; drop the '_'.
		t = append(t, 'X')
		i++
	}
	// Invariant: if the next letter is lower case, it must be converted
	// to upper case.
	// That is, we process a word at a time, where words are marked by _ or
	// upper case letter. Digits are treated as words.
	for ; i < len(s); i++ {
		c := s[i]
		if c == '_' && i+1 < len(s) && isASCIILower(s[i+1]) {
			continue // Skip the underscore in s.
		}
		if isASCIIDigit(c) {
			t = append(t, c)
			continue
		}
		// Assume we have a letter now - if not, it's a bogus identifier.
		// The next word is a sequence of characters that must start upper case.
		if isASCIILower(c) {
			c ^= ' ' // Make it a capital letter.
		}
		t = append(t, c) // Guaranteed not lower case.
		// Accept lower case sequence that follows.
		for i+1 < len(s) && isASCIILower(s[i+1]) {
			i++
			t = append(t, s[i])
		}
	}
	return string(t)
}

func unexport(s string) string {
	if len(s) == 0 {
		return ""
	}

	return strings.ToLower(s[:1]) + s[1:]
}
